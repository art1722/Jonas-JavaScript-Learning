// Number to convert null and undefined.
console.log(Number(null));
console.log(Number(undefined));
// use of ~~: The ~~ operator is also called the double-tilde or double bitwise not operator.
// It is used to floor the positive numbers which is a short form of the Math.floor() method
// but only for positive numbers.
console.log(~~null);
console.log(~~undefined);
// 0 NaN 0 0
